# There are several built-in methods in python that are used to modify strings

# The upper() method is used to return the string in upper case
a = "Hello, World!"
print(a.upper())

# The lower() method is used to return the string in lower case form
a = "Hello, World!"
print(a.lower())

# The strip() method is used to remove any whitespace from the beginning or the end of a string
a = " Hello, World!"
print(a.strip()) # returns "Hello, World!"

# The replace() method is used to replace a string with another
a = "Hello, World!"
print(a.replace("World", "Class"))

# The split() method splits the  string into substrings if it finds instances of a separator
a = "Hello, World!"
print(a.split(",")) # returns " 'Hello' , 'World!' "

